<h1 style="font-size:30px;">Frequently Asked Questions</h1>
<table width="1010" height="796" border="0">
  <tr>
    <td width="1000"><br /><br /><br /><p><strong>Q. What is VITBIFI?</strong><br />
      VITBIFI is an online web option for the students of VIT to order food from the nearby restaurants.<br />
      <br />
  <br />
      <strong>Q. Why should one order from VITBIFI?</strong><br />
      VITBIFI aims to build up long term relationship between restaurants and customers. With VITBIFI you can get your favorite foods from around the campus at your doorstep. It helps in minimizing the confusion while placing the order.
      Moreover VITBIFI will provide you many services in near future. <br />
  <br /><br />
      <strong>Q. How does VITBIFI work?</strong>
      <br />You simply order online from your favorite restaurant. You have to choose the delivery time and the delivery point.at the prescribed time a person from the restaurant will be standing there with the order and will wait for around 15 minutes. If you fail to collect your order during this time then you can avail your order from the restaurant within 1 hour from the delivery time. Even if then you fail to collect your order then your order stands as cancelled. 
      
      <br /><br /><br />
      <p><strong>Q. Why am I required to create the CAP?</strong><br />
        
      CAP is for the security of the restaurant owner. In case you do not come to take the order the amount will be deducted from the advance payment. Moreover the CAP will be the maximum limit up to which you can order online after the first week. </p>
      <p><br /><br />
      </p>
      <p><strong>Q. How can I increase or make the CAP?</strong></p>
      <p>To increase or make the CAP, you need to pay the CAP amount at 'HOT EGGS' (opposite 7ELEVEN restaurant/ beside Saleem Restaurant) between 16:00 to 21:00. You will get a receipt for the amount paid by you.</p>
      <p><br />
        <br />
        <strong>Q. Is the CAP refundable?</strong><br />
        No the CAP is not refundable. However you can order from the CAP amount when such a facility is introduced. <br />
        <br />
        <strong><br />
        Q. Do I need to pay extra to use this site?</strong><br />
        VITBIFI is absolutely FREE to use. You do have to pay the restaurants for your orders though ;) <br />
        <br />
        <br />
        <strong>Q. What are the benefits of signing up for the service?</strong><br />
        Once you are a member of the VITBIFI Community, you can avail exclusive discounts and various other benefits and offers in future. <br />
        <br />
        <br />
        <strong>Q. What is a Verified Member?</strong><br />
      Before you can order online from certain restaurants, you would need to prove that you belong to a certain group that enjoys privileges from these restaurants. For example, if you belong to an institute or organization, you can prove this by verifying your account by using the email ID provided to you by your organization. </p>
      <p>        <br />
      </p>
      <p><strong>Q. What if I do not have a VIT Gmail Id?</strong>
        <br /> 
        You can get your VIT Gmail Id from respective school office. Or you need to wait until we provide provision for other Email-Ids.
<br /><br />
        <br />
        <strong>Q. I'm worried about my privacy. How will my personal information be used?</strong><br />
    
Your privacy is of utmost importance to us. Please read our Privacy Policy for complete details. <br />
<br />
<br />
<strong>Q. Is the menu presented on VITBIFI the same as the menu offered at the restaurant?</strong><br />

Yes, we try our best to include all items at the exact same prices. However, certain items might not be deliverable (such as alcohol content, items that degrade over distance, etc.). All the prices quoted in the menu, are decided solely by the restaurant. <br />
<br />
<br />
<strong>Q. How do I make payments for the food that I order?</strong><br />

You can make the payment using cash on delivery. <br />
<br />
<br />
<strong>Q. Do I have to pay extra tips to the delivery personnel?</strong><br />

Payment of tips is at your discretion. <br />
<br />
<strong><br />
Q. I have not yet received the SMS that informs me when to come to the delivery point. What do I do?</strong><br />

It might sometime happen that you do not receive the update SMS about delivery time, either because your number is in the Do Not Disturb (DND) List or because of bad network. We suggest you to reach the delivery point on time. <br />
